
import XCTest
import Foundation
@testable import TensorSwift

class NumpyTests: XCTestCase {

    func testLoadNpyLittleFloat32() {
        guard let x = try? Tensor(npyData: NpyLittleFloat32) else {
            XCTFail()
            return
        }
        
        XCTAssertEqual(x.shape, [2, 2])
        XCTAssertEqual(x.elements, [1.0, 2.0, 3.0, 4.0])
    }
    
    func testLoadNpyLittleFloat64() {
        guard let x = try? Tensor(npyData: NpyLittleFloat64) else {
            XCTFail()
            return
        }
        
        XCTAssertEqual(x.shape, [1, 2, 2])
        XCTAssertEqual(x.elements, [1.0, 0.5, 0.25, 0.125])
    }
    
    
    func testLoadNpyBigFloat32() {
        
        guard let x = try? Tensor(npyData: NpyBigFloat32) else {
            XCTFail()
            return
        }
        
        XCTAssertEqual(x.shape, [3])
        XCTAssertEqual(x.elements, [1.0, 2.0, 3.0])
    }
    
    func testLoadNpyBigFloat64() {
        
        guard let x = try? Tensor(npyData: NpyBigFloat64) else {
            XCTFail()
            return
        }
        
        XCTAssertEqual(x.shape, [2, 2, 2])
        XCTAssertEqual(x.elements, [1.0, 0.5, 0.25, 0.125, 1.0, 2.0, 3.0, 4.0])
    }
    
    static var allTests : [(String, (NumpyTests) -> () throws -> Void)] {
        return [
            ("testLoadNpyLittle32", testLoadNpyLittleFloat32),
            ("testLoadNpyLittle64", testLoadNpyLittleFloat64),
            ("testLoadNpyBig32", testLoadNpyBigFloat32),
            ("testLoadNpyBig64", testLoadNpyBigFloat64),
        ]
    }
}

